import { swRegistration } from '@/registerServiceWorker.js'

function urlB64ToUint8Array (base64String) {
  const padding = '='.repeat((4 - base64String.length % 4) % 4)
  const base64 = (base64String + padding)
    .replace(/\-/g, '+')
    .replace(/_/g, '/')

  const rawData = window.atob(base64)
  const outputArray = new Uint8Array(rawData.length)

  for (let i = 0; i < rawData.length; ++i) {
    outputArray[i] = rawData.charCodeAt(i)
  }
  return outputArray
}

export default {
  props: {
    section: {
      type: Object,
      default: null
    },
    initiative: {
      type: Object,
      default: null
    },
    type: {
      type: String,
      default: 'SECTION'
    }
  },

  data () {
    return {
      subscriber: null,
      inheritedFrom: null,
      showError: false,
      errorMessage: 'sample'
    }
  },

  computed: {
    loggedUser () {
      if (this.$store.state.user) {
        return this.$store.state.user.profile
      }

      return null
    },

    applicableSubscriber () {
      if (this.subscriber) {
        if (this.subscriber.inheritConfig === 'CUSTOM') {
          return this.subscriber
        } else {
          return this.inheritedFrom
        }
      }
    },
    applicableSubscriberElementName () {
      if (this.inheritedFrom) {
        switch (this.inheritedFrom.elementType) {
          case 'SECTION':
            return this.$t('activity.TITLE_AND_TYPE', { title: this.inheritedFrom.section.title, type: this.$t('model.SECTION') })

          case 'INITIATIVE':
            return this.$t('activity.TITLE_AND_TYPE', { title: this.inheritedFrom.initiative.meta.name, type: this.$t('initiatives.INITIATIVE') })

          case 'COLLECTIVEONE':
            return this.$t('activity.GLOBAL_NOTIFICATIONS_PREFS')
        }
      }
    },
    isSection () {
      return this.type === 'SECTION'
    },
    notificationsTitle () {
      switch (this.type) {
        case 'SECTION':
          return this.$t('activity.EDIT_NOTIFICATIONS_OF', { title: this.section.title, type: this.$t('model.SECTION') })

        case 'INITIATIVE':
          return this.$t('activity.EDIT_NOTIFICATIONS_OF', { title: this.initiative.meta.name, type: this.$t('initiatives.INITIATIVE') })

        case 'COLLECTIVEONE':
          return this.$t('activity.EDIT_GLOBAL_NOTIFICATIONS')
      }
    },
    elementId () {
      switch (this.type) {
        case 'SECTION':
          return this.section.id

        case 'INITIATIVE':
          return this.initiative.id

        case 'COLLECTIVEONE':
          return '00000000-0000-0000-0000-000000000000'
      }
    }
  },

  methods: {
    setInherit () {
      this.subscriber.inheritConfig = 'INHERIT'
      this.updateInheritedFrom()
    },
    updateInheritedFrom () {
      /* update only the inherited from subscriber, needed when user switching to inherited */
      this.axios.get('/1/notifications/subscriber/' + this.type + '/' + this.elementId + '/inheritFrom').then((response) => {
        this.inheritedFrom = response.data.data
      })
    },
    updateSubscriber () {
      this.axios.get('/1/notifications/subscriber/' + this.type + '/' + this.elementId).then((response) => {
        this.subscriber = response.data.data
        /* initialize the inherited from subscriber if available */
        if (this.subscriber.inheritConfig === 'INHERIT') {
          this.inheritedFrom = this.subscriber.applicableSubscriber
        }
      })
    },
    closeThis () {
      this.$emit('close')
    },
    accept () {
      let ok = true

      if (this.subscriber.inheritConfig === 'CUSTOM') {
        if (this.subscriber.inAppConfig === null) {
          ok = false
          this.showError = true
          this.errorMessage = 'please configure the In-App notifications'
        }

        if (this.subscriber.pushConfig === null) {
          ok = false
          this.showError = true
          this.errorMessage = 'please configure the Push notifications'
        }

        if (this.subscriber.emailNowConfig === null) {
          ok = false
          this.showError = true
          this.errorMessage = 'please configure the immediate emails'
        }

        if (this.subscriber.emailSummaryConfig === null) {
          ok = false
          this.showError = true
          this.errorMessage = 'please configure the email summary'
        }

        if (this.subscriber.emailSummaryPeriodConfig === null) {
          ok = false
          this.showError = true
          this.errorMessage = 'please configure the email summary period'
        }
      }

      if (ok) {
        this.showError = false
        this.axios.put('/1/notifications/subscriber/' + this.type + '/' + this.elementId, this.subscriber).then((response) => {
          this.closeThis()
        })
      }
    }
  },

  mounted () {
    this.updateSubscriber()

    console.log('checking subscription')
    console.log('VUE_APP_SEVER_PUBLIC_KEY:' + process.env.VUE_APP_SEVER_PUBLIC_KEY)

    swRegistration.pushManager.getSubscription().then((subscription) => {
      if (subscription == null) {
        let applicationServerKey = urlB64ToUint8Array(process.env.VUE_APP_SEVER_PUBLIC_KEY)

        console.log('subscribing')
        swRegistration.pushManager.subscribe({
          userVisibleOnly: true,
          applicationServerKey: applicationServerKey
        }).then((pushSubscription) => {
