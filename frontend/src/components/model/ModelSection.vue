<script>
import ModelCardWithModal from '@/components/model/ModelCardWithModal.vue'
import ModelSectionModal from '@/components/model/modals/ModelSectionModal.vue'
import ModelCardModal from '@/components/model/modals/ModelCardModal.vue'

export default {
  name: 'app-model-section',

  components: {
    'app-model-section-modal': ModelSectionModal,
    'app-model-card-modal': ModelCardModal,
    'app-model-card-with-modal': ModelCardWithModal
  },

  props: {
    sectionInit: {
      type: Object,
      default: null
    },
    preloaded: {
      type: Boolean,
      default: false
    },
    initiativeId: {
      type: String,
      default: ''
    },
    sectionId: {
      type: String,
      default: ''
    },
    inView: {
      type: Boolean,
      default: false
    },
    inElementId: {
      type: String,
      default: ''
    },
    inElementTitle: {
      type: String,
      default: ''
    },
    level: {
      type: Number,
      default: 0
    },
    dragType: {
      type: String
    },
    asListItem: {
      type: Boolean,
      default: false
    },
    floating: {
      type: Boolean,
      default: false
    }
  },

  data () {
    return {
      section: null,
      showSubsections: false,
      showCards: false,
      showActionButton: false,
      showSubActionButtons: false,
      cardsAsCards: true,
      expanded: false,
      animatingSubsections: false,
      showSectionModal: false,
      showNewSubsectionModal: false,
      showNewCardModal: false
    }
  },

  computed: {
    isLoggedAnEditor () {
      return this.$store.getters.isLoggedAnEditor
    },
    showExpandButton () {
      if (!this.asListItem) {
        return ((this.section.nSubsections > 0) || (this.section.nCards > 0))
      } else {
        return false
      }
    },
    sectionTitleStyle () {
      var fontsize = this.level < 5 ? 22 - 4 * this.level : 12
      if (!this.asListItem) {
        return {'font-size': fontsize + 'px'}
      } else {
        return {'font-size': '22px'}
      }
    },
    subsectionsContainerStyle () {
      var paddingLeft = this.level < 5 ? 25 * (this.level + 1) : 100
      var marginTop = this.level < 5 ? 110 - 20 * (5 - this.level) : 10

      return {
        'padding-left': paddingLeft + 'px',
        'margin-top': marginTop + ' px'
      }
    },
    showSubsectionsButtonContent () {
      var text = ''
      if (this.showSubsections) {
        text = ' hide subsection'
        if (this.section.subsections.length > 1) {
          text += 's'
        }
      } else {
        text = '<i class="fa fa-caret-right" aria-hidden="true"></i><b> ' + this.section.subsections.length + '</b> subsection'
        if (this.section.subsections.length > 1) {
          text += 's'
        }
      }
      return text
    },
    sectionIsInOtherPlaces () {
      var ix
      for (ix in this.section.inSections) {
        if (this.section.inSections[ix].id !== this.inElementId) {
          return true
        }
      }

      for (ix in this.section.inViews) {
        if (this.section.inViews[ix].id !== this.inElementId) {
          return true
        }
      }

      return false
    }
  },

  watch: {
    '$store.state.support.triggerUpdateModel' () {
      this.update()
    }
  },

  methods: {
    toggleExpand () {
      if (!this.expanded) {
        if (!this.preloaded) {
          this.update()
        }
        this.expanded = true
      } else {
        this.expanded = false
      }
    },
    update () {
      this.axios.get('/1/initiative/' + this.initiativeId + '/model/section/' + this.section.id, {
        params: {
          level: 1
        }
      }).then((response) => {
        this.section = response.data.data
        this.checkExpands()
      })
    },
    checkExpands () {
      if (this.section.nSubsections > 0 && this.section.cardsWrappers.length === 0) {
        this.showSubsections = true
      }
    },
    expandSectionModal () {
      this.showSectionModal = true
    },
    newSubsection () {
      this.showNewSubsectionModal = true
    },
    newCard () {
      this.showNewCardModal = true
    },
    cardDroped (onCardWrapperId, event) {
      var dragData = JSON.parse(event.dataTransfer.getData('text/plain'))

      if (dragData.type === 'MOVE_CARD') {
        var url = '/1/initiative/' + this.initiativeId +
        '/model/section/' + dragData.fromSectionId +
        '/moveCard/' + dragData.cardWrapperId

        this.axios.put(url, {}, {
          params: {
            onSectionId: this.section.id,
            onCardWrapperId: onCardWrapperId
          }
        }).then((response) => {
          this.$store.commit('triggerUpdateModel')
        })
      }
    },
    dragStart (event) {
      var moveSectionData = {
        type: this.dragType,
        sectionId: this.section.id,
        fromView: this.inView,
        fromElementId: this.inElementId
      }
      event.dataTransfer.setData('text/plain', JSON.stringify(moveSectionData))
    },
    subsectionDroped (onSubsectionId, event) {
      var dragData = JSON.parse(event.dataTransfer.getData('text/plain'))
      var url = ''

      if (dragData.type === 'MOVE_SECTION') {
        /* move section to view section */
        url = '/1/initiative/' + this.initiativeId +
          '/model/view/' + dragData.inElementId +
          '/moveSection/' + dragData.sectionId
      }

      if (dragData.type === 'MOVE_SUBSECTION') {
        url = '/1/initiative/' + this.initiativeId +
          '/model/section/' + dragData.inElementId +
          '/moveSubsection/' + dragData.sectionId
      }

      if (url !== '') {
        this.axios.put(url, {}, {
          params: {
            onSectionId: this.section.id,
            onSubsectionId: onSubsectionId
          }
        }).then((response) => {
          this.$store.commit('triggerUpdateModel')
        })
      }
    }
  },

  created () {
    if (!this.preloaded) {
      this.section = {
        id: this.sectionId
      }
      this.update()
    } else {
      this.section = this.sectionInit
      this.checkExpands()
    }
  }
}
</script>

